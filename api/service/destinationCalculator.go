/**

Assumed the following scheduling of destinations

 _______________________________________________________
|                                                       |
|   Destinations ids for Day/Pad combinations           |
|_______________________________________________________|
|      \      |     |     |     |     |     |     |     |
|  Pad  \ Day | MO  | TU  | WE  | TH  | FR  | SA  | SU  |
|________\____|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P1   | D1  | D2  | D3  | D4  | D5  | D6  | D7  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P2   | D7  | D1  | D2  | D3  | D4  | D5  | D6  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P3   | D6  | D7  | D1  | D2  | D3  | D4  | D5  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P4   | D5  | D6  | D7  | D1  | D2  | D3  | D4  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P5   | D4  | D5  | D6  | D7  | D1  | D2  | D3  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P6   | D3  | D4  | D5  | D6  | D7  | D1  | D2  |
|_____________|_____|_____|_____|_____|_____|_____|_____|
|             |     |     |     |     |     |     |     |
|        P7   | D2  | D3  | D4  | D5  | D6  | D7  | D1  |
|_____________|_____|_____|_____|_____|_____|_____|_____|


*/
func calcDestinationForDayAndLaunchpad(weekdayNo uint, launchpadId uint) uint {
	destinationId := (weekdayNo + 8 - launchpadId) % 7
	return (destinationId + 6) % 7 + 1
}


